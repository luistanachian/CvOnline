using Cv.Business.Class;
using Cv.Models;
using Cv.Repository.Interface;
using Cv.Test.MockModel;
using Moq;
using NUnit.Framework;
using System;
using System.Collections.Generic;

namespace Cv.Test
{
    public class Test_CandidateBusiness
    {

        [Test]
        public void Replace()
        {
            var candidate = CandidateMockModel.CandidateOk(); 
            var mockCandidate = new Mock<ICandidatesRepository>();
            mockCandidate.Setup(c => c.Replace(It.IsAny<CandidateModel>())).Returns(true); 
            var bus = new CandidatesBusiness(mockCandidate.Object);
            var replaced = bus.Replace(candidate);
            Assert.AreEqual(true, replaced);
            mockCandidate.Verify(c => c.Replace(It.IsAny<CandidateModel>()));
        }
        [Test]
        public void Insert()
        {
            var candidate = CandidateMockModel.CandidateOk(); 
            var mockCandidate = new Mock<ICandidatesRepository>();
            mockCandidate.Setup(c => c.Insert(It.IsAny<CandidateModel>())); 
            var bus = new CandidatesBusiness(mockCandidate.Object);
            var inserted = bus.Insert(candidate);
            Assert.AreEqual(true, inserted);
            mockCandidate.Verify(c => c.Insert(It.IsAny<CandidateModel>()));
        }
        [Test]
        public void GetAllByCompanyId()
        {
            var companyId = Guid.NewGuid().ToString(); ;
            var mockCandidate = new Mock<ICandidatesRepository>();
            mockCandidate.Setup(c => c.GetAllByCompanyId(It.IsAny<string>())).Returns(new List<CandidateModel>());
            var bus = new CandidatesBusiness(mockCandidate.Object);
            var list = bus.GetAllByCompanyId(companyId);
            Assert.AreEqual(true, list != null);
            mockCandidate.Verify(c => c.GetAllByCompanyId(companyId));
        }

        [Test]
        public void Delete()
        {
            var candidateId = Guid.NewGuid().ToString(); ;
            var mockCandidate = new Mock<ICandidatesRepository>();
            mockCandidate.Setup(c => c.Delete(It.IsAny<string>())).Returns(true);
            var bus = new CandidatesBusiness(mockCandidate.Object);
            var deleted = bus.Delete(candidateId);
            Assert.AreEqual(true, deleted);
            mockCandidate.Verify(c => c.Delete(candidateId));
        }
    }
}